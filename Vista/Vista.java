    /**
     * Detecta los botones del teclado como los botones de la pantalla para que sea mas eficiente
     * @param evt 
     */
    private void JTKeyListenerKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JTKeyListenerKeyPressed
        
        String i =  String.valueOf(evt.getKeyChar());
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            i = "";
        }
        if (evt.getKeyCode() == 8) {
            i = "delete";
        }
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            System.exit(0);
        } 
        
        switch (i) {
            case "1" -> Controlador.meterNumero(1);
            case "2" -> Controlador.meterNumero(2);
            case "3" -> Controlador.meterNumero(3);
            case "4" -> Controlador.meterNumero(4);
            case "5" -> Controlador.meterNumero(5);
            case "6" -> Controlador.meterNumero(6);
            case "7" -> Controlador.meterNumero(7);
            case "8" -> Controlador.meterNumero(8);
            case "9" -> Controlador.meterNumero(9);
            case "0" -> Controlador.meterNumero(0);
            case "+" -> Controlador.hacerOperacion("+");
            case "-" -> Controlador.hacerOperacion("-");
            case "*" -> Controlador.hacerOperacion("x");
            case "/" -> Controlador.hacerOperacion("รท");
            case "" -> Controlador.hacerOperacion(null);
            case "." -> Controlador.meterComa();
            case "delete" -> Controlador.borrar(false);
            
        }
    }//GEN-LAST:event_JTKeyListenerKeyPressed

